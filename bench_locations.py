def tuples_for_benches(days_dict):
    day_list = []
    keylist = days_dict.keys()
    keylist.sort()
    for k in keylist:
    	#final_dict = {}
    	bench_positions = []
    	images = days_dict[k]
    	image_keys = images.keys()
    	image_keys.sort()
    	#image_keys = sorted(images, key=lambda k:k['img_timestamp'])
    	init_benches = images[image_keys[0]].values()[0]
    	init_time = images[image_keys[0]].keys()[0]
    	#init_benches = images[0].values()[0]
    	bench_positions.append([init_time, init_benches])
    	for i in image_keys[1:]:
    		moved_benches = images[i].values()[0]
    		#benches = moved_benches.values()
    		time = images[i].keys()[0]
    		keys = moved_benches.keys()
    		keys.sort()
    		new_benches = dict(bench_positions[-1][1])
    		if(len(keys) > 1):
    			for j, l in enumerate(keys):
    				new_benches[l] = moved_benches[l]
    		else:
    			if(moved_benches[keys[0]] != 'disappear'):
    				#print moved_benches[keys[0]]
    				new_benches[keys[0]] = moved_benches[keys[0]]

    			else:
    				del new_benches[keys[0]]
    		bench_positions.append([time, new_benches])

    	day_list.append((k, bench_positions))
    return day_list



